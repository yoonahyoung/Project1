#include<stdio.h>


int main() {

	for (int i = 0; i < 10; i++) {
		for (int j = 0; j < 10; j++) {
			printf("*");
		}
		printf("\n");
	}

	printf("\n");

	for (int i = 0; i < 10; i++) {
		for (int j = 0; j < 10; j++) {
			if (i == 0 || i == 9 || j == 0 || j == 9) {
				printf("*");
			}
			else {
				printf(" ");
			}
		}printf("\n");
	}

	printf("\n");

	for (int i = 0; i < 10; i++) {
		for (int j = 0; j <= i; j++) {
			printf("*");
		}
		printf("\n");
	}


	printf("\n");


	for (int i = 0; i < 10; i++) {
		for (int j = 9; j >= i; j--) {
			printf("*");
		}
		printf("\n");
	}



	printf("\n");




	for (int i = 0; i < 10; i++) {
		for (int j = 9; j >= i; j--) {
		printf(" ");
		}
		for (int k = 0; k <= 2* (i-1) +2 ; k++) {
		printf("*");
		}
		printf("\n");
	}




	return 0;
}



